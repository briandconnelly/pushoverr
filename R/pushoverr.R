#' pushoverr: Send push notifications using Pushover
#'
#' pushoverr is package for sending push notifications to mobile devices
#' (iOS and Android) and the desktop using
#' [Pushover](https://pushover.net). Messages can be quickly sent using the
#' [`pushover`] function. Apple Watch users can push small bits of
#' data to their watches using [`update_glance`].
#'
#' To use pushoverr, you will first need your *user key* and an
#' *application token*. The user key can be found within the settings of
#' the app (e.g., `uQiRzpo4DXghDmr9QzzfQu27cmVRsG`) or by logging in to
#' [https://pushover.net]. To receive an application token, register a new
#' application after logging in to [https://pushover.net/apps].
#'
